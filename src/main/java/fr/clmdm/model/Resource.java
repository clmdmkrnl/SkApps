package fr.clmdm.model;

public class Resource {
	
	private String nom;

	private String prenom;

	private SopraProfil profil;

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}

	public SopraProfil getProfil() {
		return profil;
	}

	public void setProfil(SopraProfil profil) {
		this.profil = profil;
	}

	
}
